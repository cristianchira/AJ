package Basic_Principles_OOP.Polimorfism;

/*                                                    Polymorphism

        is defined as "Same code" giving "Different behaviour"

        is the ability of an object to take on many forms.The most common use of polymorphism in OOP occurs when a parent
        class reference is used to refer to a child class object.

        Any Java object that can pass more than one IS-A test is considered to be polymorphic. In Java, all Java objects
        are polymorphic since any object will pass the IS-A test for their own type and for the class Object.

*/