package Basic_Principles_OOP.Encapsulation;
/*
                                     	Encapsulation

    Encapsulation is “hiding the implementation of a Class behind a well defined interface”.

    Other way to think about encapsulation :
       it is a protective shield that prevents the data from being accessed by the code outside this shield.

                                 Benefits of Encapsulation

    The fields of a class: ---------------------------------------------------------------------------------------------
        can be made read-only or write-only.
    A class can have total control -------------------------------------------------------------------------------------
        over what is stored in its fields.
    Encapsulation helps us to change implementation of a class ---------------------------------------------------------
       without breaking other code.


*/