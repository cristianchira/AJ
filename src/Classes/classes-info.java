package Classes;

/*
                                   abstract Classes

          It is a compile-time error if an attempt is made to create an instance of an abstract class

                                     final Classes

          A class can be declared final if its definition is complete and no subclasses are desired or required.
          A final class cannot have any subclasses.

                                 strictfp Classes

          The effect of the strictfp modifier is to make all float or double expressions within the class declaration
          (including within variable initializers, instance initializers, static initializers, and constructors)
          This implies that all methods declared in the class, and all nested types declared in the class, are
          implicitly strictfp.

                          Generic Classes and Type Parameters

          A class is generic if it declares one or more type variables (§4.4).

                            Inner Classes and Enclosing Instances

           An inner class is a nested class that is not explicitly or implicitly declared static.
           An inner class may be a non-static member class, a local class , or an anonymous class .

                                   Class Members

        The members of a class type are all of the following:

        Members inherited from its direct superclass , except in class Object, which has no direct superclass

        Members inherited from any direct superinterfaces

        Members declared in the body of the class

        */